nUm = float(input('Informe o primeiro número: '))
nDois = float(input('Informe o segundo número: '))

soma = nUm + nDois 
subtracao = nUm - nDois 
multiplicacao = nUm * nDois 
divisao = nUm / nDois 

print(f'{nUm} + {nDois} = {soma}')
print(f'{nUm} - {nDois} = {subtracao}')
print(f'{nUm} * {nDois} = {multiplicacao}')
print(f'{nUm} / {nDois} = {divisao}')